//! # `sync` — offline-first cache + server synchronization
//!
//! A client-side cache layer that synchronizes server artifacts with a
//! device. The app:
//!
//! 1. **Downloads** a named slice of server data — a *partition* (e.g.
//!    `"project:123"`) — into the local cache.
//! 2. **Reads and mutates** it offline; the UI binds to a reactive
//!    `Signal` per partition.
//! 3. **Reconnects**, at which point queued mutations replay (the durable
//!    *outbox*) and server changes reconcile against local edits through a
//!    pluggable per-entity [`Merge`] policy.
//!
//! ## Shape
//!
//! The SDK is **client-side bookkeeping over two RPCs** —
//! [`pull`](protocol::PullRequest) and [`push`](protocol::PushRequest),
//! generic over the app's entity `T`. The app owns its entity types, the
//! server bodies of those two endpoints, and the `Merge` policy; the SDK
//! owns persistence, the outbox, the sync engine, and the protocol. The
//! app connects the two by implementing [`Transport`] (typically generated
//! by the `sync_entity!` macro) — which is why this crate has **no
//! dependency on `server`**: the protocol *types* are the only contract.
//!
//! ## Correctness model (v1)
//!
//! *Correct* means **no silent data loss, crash-safe across restarts, and
//! conflicts surfaced to the app** — under a single-writer-per-device
//! assumption. It does **not** mean automatic multi-device convergence,
//! real-time push, or CRDTs; those layer on top of the same cursor +
//! outbox + merge primitives without reworking them.
//!
//! The crash-safety model rests on one fact about the [`storage`] backing:
//! a single `set` is atomic, nothing spanning two keys is. So every state
//! transition commits via exactly one `set`, and everything else is
//! reconstructed on boot. The invariant, in one line: **the outbox commits
//! before the UI acknowledges; record-state commits before the outbox
//! pops; record-data commits before the cursor advances.**

