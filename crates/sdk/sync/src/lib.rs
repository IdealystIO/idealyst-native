/// Generate a [`Transport`] impl that wires the engine to a pair of
/// app-authored `pull`/`push` server functions.
///
/// The app writes the two `#[server]` fns (with the real data access in
/// their bodies) and this macro removes the boilerplate of bridging them
/// to the engine — mapping the server fn's error to [`SyncError`].
///
/// ```ignore
/// // The app's two server fns over its entity `Project`:
/// #[server]
/// pub async fn pull_projects(req: sync::PullRequest)
///     -> Result<sync::PullResponse<Project>, ServerError> { /* DB read */ }
/// #[server]
/// pub async fn push_projects(req: sync::PushRequest<Project>)
///     -> Result<sync::PushResponse<Project>, ServerError> { /* DB write */ }
///
/// // One line wires them into a Transport the engine can call:
/// sync::sync_transport!(ProjectTransport, Project, pull = pull_projects, push = push_projects);
///
/// let transport = std::rc::Rc::new(ProjectTransport);
/// let projects = engine.partition::<Project>("project:123", transport).await?;
/// ```
///
/// Any error from the server fn (network, server 5xx, schema drift) is
/// mapped to [`SyncError::Transport`] via its `Display`. An app that wants
/// to distinguish an incompatible-schema (HTTP 426) response as
/// [`SyncError::IncompatibleVersion`] can detect it inside its own server
/// fn wrapper before returning.
#[macro_export]
macro_rules! sync_transport {
    ($transport:ident, $entity:ty, pull = $pull:path, push = $push:path $(,)?) => {
        /// Engine-to-server bridge generated by [`sync::sync_transport!`].
        pub struct $transport;

        impl $crate::Transport<$entity> for $transport {
            fn pull(
                &self,
                req: $crate::PullRequest,
            ) -> $crate::TransportFuture<'_, $crate::PullResponse<$entity>> {
                ::std::boxed::Box::pin(async move {
                    $pull(req)
                        .await
                        .map_err(|e| $crate::SyncError::Transport(::std::string::ToString::to_string(&e)))
                })
            }

            fn push(
                &self,
                req: $crate::PushRequest<$entity>,
            ) -> $crate::TransportFuture<'_, $crate::PushResponse<$entity>> {
                ::std::boxed::Box::pin(async move {
                    $push(req)
                        .await
                        .map_err(|e| $crate::SyncError::Transport(::std::string::ToString::to_string(&e)))
                })
            }
        }
    };
}
