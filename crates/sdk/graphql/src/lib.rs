/// Generate a [`Transport`] that bridges an app-authored server function
/// into the GraphQL client — the "works with server functions, without
/// depending on them" seam.
///
/// The app writes one `#[server] async fn(GraphqlRequest) -> Result<GraphqlResponse, E>`
/// (any `E: Display`, e.g. `ServerError`) and this macro removes the
/// boilerplate of adapting it to [`Transport`], mapping the server-fn error
/// to [`GraphqlError::Transport`]. The direct analogue of `sync`'s
/// `sync_transport!`.
///
/// ```ignore
/// // The app's server fn (server build runs the body; client build is a stub):
/// #[server]
/// pub async fn graphql_endpoint(req: graphql::GraphqlRequest)
///     -> Result<graphql::GraphqlResponse, ServerError> { /* execute_request */ }
///
/// // One line wires it into a client transport on both builds:
/// graphql::graphql_transport!(AppGraphql, graphql_endpoint);
/// let client = graphql::GraphqlClient::new(std::rc::Rc::new(AppGraphql));
/// ```
#[macro_export]
macro_rules! graphql_transport {
    ($transport:ident, $endpoint:path $(,)?) => {
        /// Server-function-backed GraphQL transport generated by
        /// [`graphql::graphql_transport!`].
        pub struct $transport;

        impl $crate::Transport for $transport {
            fn execute(
                &self,
                request: $crate::GraphqlRequest,
            ) -> $crate::TransportFuture<'_, $crate::GraphqlResponse> {
                ::std::boxed::Box::pin(async move {
                    $endpoint(request).await.map_err(|e| {
                        $crate::GraphqlError::Transport(::std::string::ToString::to_string(&e))
                    })
                })
            }
        }
    };
}
