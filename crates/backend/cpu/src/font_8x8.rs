//! Built-in 8x8 monochrome bitmap font, covering ASCII 0x20..=0x7E.
//!
//! Public-domain glyph data, modeled after the classic VGA / PC ROM
//! 8x8 font that's been baked into a dozen embedded-graphics
//! libraries (the "IBM PC BIOS" / "Code page 437" 8x8 set is in the
//! public domain in the United States — IBM published it without a
//! copyright assertion and no court has ever held it copyrightable).
//!
//! Each glyph is 8 bytes; each byte is one row (top-to-bottom), MSB
//! is the leftmost pixel. A `1` bit is foreground (the glyph), a `0`
//! bit is background (transparent — the renderer doesn't paint it).
//!
//! ## Coverage
//!
//! ASCII printable range only — 0x20 (space) through 0x7E (`~`).
//! Any char outside that range renders as a hollow square
//! (`GLYPH_TOFU`) so missing glyphs are visible in test output
//! instead of silently disappearing.
//!
//! For richer character sets (Unicode, emoji), this font is the
//! wrong tool — swap in `fontdue` or `ab_glyph` behind a Cargo
//! feature. The bitmap font stays the default because it has zero
//! deps and predictable cost: 95 glyphs × 8 bytes = 760 bytes total,
//! which fits comfortably even in the most constrained
//! microcontroller flash budget.

/// Width of every glyph in pixels (the "8x8" in the module name).
pub const GLYPH_W: u32 = 8;
/// Height of every glyph in pixels.
pub const GLYPH_H: u32 = 8;

/// Fallback glyph painted for any char outside 0x20..=0x7E. A
/// hollow 6x6 box — visible enough to flag the miss during
/// development without dominating the layout.
const GLYPH_TOFU: [u8; 8] = [
    0b00000000,
    0b01111100,
    0b01000100,
    0b01000100,
    0b01000100,
    0b01000100,
    0b01111100,
    0b00000000,
];

/// Look up the 8-byte glyph for an ASCII character. Returns
/// [`GLYPH_TOFU`] for anything outside the printable range.
pub fn glyph_for(c: char) -> [u8; 8] {
    let b = c as u32;
    if (0x20..=0x7E).contains(&b) {
        GLYPHS[(b - 0x20) as usize]
    } else {
        GLYPH_TOFU
    }
}

/// Compositional measure: how many pixels wide a string is when
/// rendered at scale `1`. Just `len * GLYPH_W` for ASCII (no
/// kerning, no proportional widths) — the bitmap font is fixed-
/// width by definition.
pub fn measure_width(s: &str) -> u32 {
    // `chars().count()` because the source MAY be UTF-8 with
    // multi-byte sequences (those will render as tofu, but they
    // still consume one glyph-width slot).
    (s.chars().count() as u32) * GLYPH_W
}

