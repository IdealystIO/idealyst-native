#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn glyph_table_length_matches_ascii_range() {
        assert_eq!(GLYPHS.len(), (0x7E - 0x20 + 1) as usize);
    }

    #[test]
    fn space_is_blank() {
        assert_eq!(glyph_for(' '), [0; 8]);
    }

    #[test]
    fn non_ascii_falls_back_to_tofu() {
        assert_eq!(glyph_for('€'), GLYPH_TOFU);
        assert_eq!(glyph_for('\u{0}'), GLYPH_TOFU);
    }

    #[test]
    fn measure_width_is_glyphs_times_eight() {
        assert_eq!(measure_width(""), 0);
        assert_eq!(measure_width("abc"), 24);
        assert_eq!(measure_width("hello"), 40);
    }
}
